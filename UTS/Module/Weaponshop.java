package UTS.Module;

public class Weaponshop{
    private String NamaSnjt;
    private String Tipeserang;
    private int Mana;
    private int Atk;

    public String getNamaSnjt() {
        return NamaSnjt;
    }
    public void setNamaSnjt(String namaSnjt) {
        NamaSnjt = namaSnjt;
    }
    public String getTipeserang() {
        return Tipeserang;
    }
    public void setTipeserang(String tipeserang) {
        Tipeserang = tipeserang;
    }
    public int getAtk() {
        return Atk;
    }
    public void setAtk(int atk) {
        Atk = atk;
    }
    
    public int getMana() {
        return Mana;
    }
public void setMana(int mana) {
        Mana = mana;
    }
}
